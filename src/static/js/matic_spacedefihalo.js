async function main() {
    const App = await init_ethers();

    _print(`Initialized ${App.YOUR_ADDRESS}\n`);
    _print("Reading smart contracts...\n");

    const SPACEDEFIHALO_CHEF_ADDR = "0xB7825c403D66C58e772b1b401c73f4EB3c53BDEe";
    const rewardTokenTicker = "HALO";
    const SPACEDEFIHALO_CHEF = new ethers.Contract(SPACEDEFIHALO_CHEF_ADDR, SPACEDEFIHALO_CHEF_ABI, App.provider);

    const startBlock = await SPACEDEFIHALO_CHEF.startBlock();
    const currentBlock = await App.provider.getBlockNumber();

    let rewardsPerWeek = 0
    if (currentBlock < startBlock) {
        _print(`Rewards start at block ${startBlock}\n`);
    } else {
        rewardsPerWeek = await SPACEDEFIHALO_CHEF.hloPerBlock() / 1e18 *
            604800 / 2.1;
    }

    const tokens = {};
    const prices = await getMaticPrices();

    await loadMaticChefContract(App, tokens, prices, SPACEDEFIHALO_CHEF, SPACEDEFIHALO_CHEF_ADDR, SPACEDEFIHALO_CHEF_ABI, rewardTokenTicker,
        "halo", null, rewardsPerWeek, "pendingHlo");

    hideLoading();
}
